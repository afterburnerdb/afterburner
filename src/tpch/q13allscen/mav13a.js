function query13a_mav(){
  var subq=select()
            .open("@o_comment")
            .from("@customer")
            .ljoin("@orders")
              .on(eq("@c_custkey","@o_custkey"))
            .field("@c_custkey",as(count("@o_orderkey"),"c_count"))
            .group("@c_custkey")

  return select()
            .from(subq)
            .field("@c_count",as(count("@*"),"custdist"))
            .group("@c_count")
            .order("-@custdist","-@c_count")
}
