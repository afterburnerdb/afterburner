function query6a_mav(){

  return select()
    .open("@l_shipdate")
    .from("@lineitem")
    .field(as(sum(mul("@l_extendedprice","@l_discount")),"revenue"))
    .where(gte("@l_discount",0.0499999),
      lte("@l_discount",0.0700001),
      lt ("@l_quantity",24));
}
