function query6b_mav(){

  return select()
    .open("@l_discount")
    .from("@lineitem")
    .field(as(sum(mul("@l_extendedprice","@l_discount")),"revenue"))
    .where(gte("@l_shipdate",date('1994-01-01')),
      lt ("@l_shipdate",date('1995-01-01')),
      lt ("@l_quantity",24));
}
