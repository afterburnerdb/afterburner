function query6(){
  return  ABi.select()
    .from("lineitem")
    .field(_as(_sum(_mul("l_extendedprice","l_discount")),"revenue"))
    .where(_gte("l_shipdate",_date('1994-01-01')),
      _lt("l_shipdate",_date('1995-01-01')),
      _gte("l_discount",0.0499999),
      _lte("l_discount",0.0700001),
      _lt("l_quantity",24))
}
