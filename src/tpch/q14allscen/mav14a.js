function query14a_mav(){
  return select()
    .open("@l_shipdate")
    .from("@lineitem","@part")
    .field(as(mul(100.00,div(sumif(mul("@l_extendedprice", sub(1,"@l_discount")),like("@p_type", 'PROMO%')), 
           sum(mul("@l_extendedprice", sub(1,"@l_discount"))))),"promo_revenue"))
    .where(eq("@l_partkey","@p_partkey"));
}
